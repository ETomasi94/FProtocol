    private void EncodeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EncodeButtonMouseClicked

            String key;
            
            if(TextFieldStatus == 0)
            {
                key = "";
                TextFieldStatus = 1;
                KeyNameTextField.setText("");
            }
            else
            {
                key = KeyNameTextField.getText();
                KeyNameTextField.setText("");
            }
            
            String text = Message.getText();
            message.write(text);
            message.encode(key);
            text = message.read();
            Message.setText(text);
            
            message.Save();
            
            SavePath.setText("MESSAGGIO SALVATO IN: "+System.getProperty("user.dir"));
    }//GEN-LAST:event_EncodeButtonMouseClicked

    private void DecodeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DecodeButtonMouseClicked
        String Guess = KeyNameTextField.getText();
        
        int decoding = message.decode(Guess);
        
        if(decoding == -1)
        {
            Message.setForeground(Color.red);
            Message.setText("ACCESSO AL MESSAGGIO NEGATO! RIPROVARE");
        }
        else
        {
            Message.setText(message.message);
        }
    }//GEN-LAST:event_DecodeButtonMouseClicked

